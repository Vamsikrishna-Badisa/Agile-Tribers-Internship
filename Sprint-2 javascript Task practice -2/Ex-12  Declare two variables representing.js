// Declare two boolean variables
let isSunny = true;
let isWeekend = false;

// Logical AND (&&): true only if both are true
console.log("isSunny && isWeekend:", isSunny && isWeekend); // false

// Logical OR (||): true if at least one is true
console.log("isSunny || isWeekend:", isSunny || isWeekend); // true

// Logical NOT (!): inverts the boolean value
console.log("!isSunny:", !isSunny);       // false
console.log("!isWeekend:", !isWeekend);   // true
