// Declare a variable
let count = 10;
console.log("Initial value:", count);

// Increment the value by 1
count++; // equivalent to: count = count + 1
console.log("After increment (count++):", count);

// Decrement the value by 1
count--; // equivalent to: count = count - 1
console.log("After decrement (count--):", count);

// You can also use prefix increment/decrement
++count;
console.log("After prefix increment (++count):", count);

--count;
console.log("After prefix decrement (--count):", count);
